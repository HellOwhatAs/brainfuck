fn main() {
    let mut pc = 0;
    let mut mem = vec![];

    (pc, mem) = brain_fuck!(
        ++++++++[>++++[>++>+++>+++>+<<<<-]>+>+>->>+[<]<-]>>.
        >---.+++++++..+++.>>.<-.<.+++.------.--------.>>+.>++.
    ).env(pc, mem);
    println!("{:?}", (pc, mem));

    (pc, mem) = brain_fuck!(
        ++++++++[>+>++++<<-]>++>>+<[-[>>+<<-]+>>]>+[
            -<<<[
                ->[+[-]+>++>>>-<<]<[<]>>++++++[<<+++++>>-]+<<++.[-]<<
            ]>.>+[>>]>+
        ]
    ).into();
    println!("{:?}", (pc, mem));
}